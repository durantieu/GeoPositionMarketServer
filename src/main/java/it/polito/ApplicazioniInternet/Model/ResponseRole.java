package it.polito.ApplicazioniInternet.Model;

public class ResponseRole {
    String id;
    String role;

    public ResponseRole() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
