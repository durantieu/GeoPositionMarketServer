package it.polito.ApplicazioniInternet.Model;

public class InfoUser {
    String name;
    int wallet;

    public InfoUser() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getWallet() {
        return wallet;
    }

    public void setWallet(int wallet) {
        this.wallet = wallet;
    }
}
